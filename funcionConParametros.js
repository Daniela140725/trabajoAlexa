function saludar(nombre, edad) {
    return `Hola ${nombre}, tienes ${edad} años.`;
}

console.log(saludar('Daniela', 18));

/*
1.	¿Qué son parámetros?
Son las variables que se definen en la función
2.	¿Qué son argumentos?
Son los valores que se envían cuando se llama la función
3.	¿Cuál es la diferencia entre ambos?
Parámetros = lo que recibe la función
Argumentos = lo que le pasamos a la función
*/